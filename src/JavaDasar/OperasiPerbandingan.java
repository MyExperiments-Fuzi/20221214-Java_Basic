package JavaDasar;

public class OperasiPerbandingan {

  public static void main(String[] args) {
    // Operasi Perbandingan = operasi yg membandingkan dua buah data, menghasilkan nilai boolean (benar atau salah)
    int a = 100;
    int b = 100;

    System.out.println(a > b);
    System.out.println(a < b);
    System.out.println(a >= b);
    System.out.println(a <= b);
    System.out.println(a == b);
    System.out.println(a != b);
  }
}
