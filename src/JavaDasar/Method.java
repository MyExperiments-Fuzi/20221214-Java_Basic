package JavaDasar;

public class Method {

  public static void main(String[] args) {
    /*
     * Method = block kode program yg akan berjalan saat kita panggil
     * Sebelumnya kita sudah menggunakan method println() untuk menampilkan tulisan di console
     * Untuk membuat method di Java, kita bisa menggunakan kata kunci void, lalu diikuti dengan nama method, kurung() dan akhiri dengan blok.
     * Kita bisa memanggil method dengan menggunakan nama method lalu diikuti dengan kurung ().
     * Di bahasa pemrograman lain, Method juga disebut sebagai Function.
     */

    // Kode: Method
    sayHelloWorld();
  }

  static void sayHelloWorld() {
    System.out.println("Hello World 1");
    System.out.println("Hello World 2");
    System.out.println("Hello World 3");
  }
}
