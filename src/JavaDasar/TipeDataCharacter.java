package JavaDasar;

public class TipeDataCharacter {

  public static void main(String[] args) {
    // Dengan menggunakan petik satu ''

    char f = 'F';
    char u = 'U';
    char a = 'A';
    char d = 'D';

    // menggunakan println = memberikan enter
    System.out.println(f);
    System.out.println(u);
    System.out.println(a);
    System.out.println(d);

    System.out.println("");
    // menggunakan print = tidak akan enter
    System.out.print(f);
    System.out.print(u);
    System.out.print(a);
    System.out.print(d);

    System.out.println("");
  }
}
